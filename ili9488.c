void drawPixel(int16_t x, int16_t y, uint16_t color)
{
	if ((x < 0) || (x >= width) || (y < 0) || (y >= height))
		return;

	uint8_t r = (color & 0xF800) >> 11;
	uint8_t g = (color & 0x07E0) >> 5;
	uint8_t b = color & 0x001F;

	r = (r * 255) / 31;
	g = (g * 255) / 63;
	b = (b * 255) / 31;
	uint8_t data[3] = {r, g, b};
	setAddrWindow(x, y, x + 1, y + 1);
	DC_DATA();
	CS_LOW();

	ILI9488_SendData_Multi(data, 3);
	CS_HIGH();

}


void drawChar(int16_t x, int16_t y, unsigned char c, uint16_t color, uint16_t bg)
{
	int size = 1;
	if(rotationNum == 1 || rotationNum ==3)
	{
		if((x >= ILI9488_WIDTH)            || // Clip right
     (y >= ILI9488_HEIGHT))   // Clip top
    return;
	}
	else
	{
		if((y >= ILI9488_WIDTH)            || // Clip right
     (x >= ILI9488_HEIGHT))   // Clip top
    return;
	}


  if(!_cp437 && (c >= 176)) c++; // Handle 'classic' charset behavior

  for (int8_t i=0; i<5; i++ ) {
    uint8_t line = pgm_read_byte(&(font1[c*5 +i]));
    for (int8_t j = 0; j<8; j++) {
      if (line & 0x01) {
        drawPixel(x+i, y+j, color);
      } else if (bg != color) {
        drawPixel(x+i, y+j, bg);
      }
      line >>= 1;
    }
  }
}

void drawBox(int16_t x, int16_t y, int16_t width, int16_t height, uint16_t color)
{
	int borderSize = 10;
    // Top side
    for (int16_t i = 0; i < borderSize; i++)
    	for (int16_t j = 0; j < width; j++)
    		drawPixel(x + j, y + i, color);

    // Bottom side
    for (int16_t i = 0; i < borderSize; i++)
    	for (int16_t j = 0; j < width; j++)
    		drawPixel(x + j, y + height - 1 - i, color);

    // Left side
    for (int16_t i = 0; i < borderSize; i++)
    	for (int16_t j = 0; j < height; j++)
    		drawPixel(x + i, y + j, color);

    // Right side
    for (int16_t i = 0; i < borderSize; i++)
    	for (int16_t j = 0; j < height; j++)
    		drawPixel(x + width - 1 - i, y + j, color);
}

