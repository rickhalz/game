	DC_DATA();
	CS_LOW();
	d=(color>>8) | (color<<8);
	for (i=0; i < ILI9488_WIDTH * ILI9488_HEIGHT ; i++){
		HAL_SPI_Transmit(&hspi1, (uint8_t *)&d, 2, 100);
	}
