void ILI9488_SendCommand(uint8_t com)
{
	//*(__IO uint8_t *)(0x60000000) = com;
	uint8_t tmpCmd = com;
	//Set DC HIGH for COMMAND mode
	DC_COMMAND();    //Put CS LOW
	CS_A();	//Write byte using SPI
	HAL_SPI_Transmit(&hspi1, &tmpCmd, 1, 1);
	//SendByte(tmpCmd);
	//WaitLastData();
    //Bring CS HIGH
	CS_D();
}

